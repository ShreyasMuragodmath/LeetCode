class Solution(object):
    def removeDuplicates(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        l,r = 0,0
        while r < len(nums):
            nums[l] = nums[r]
            while r < len(nums) and nums[l] == nums[r]:
                r += 1
            l += 1
        return l

